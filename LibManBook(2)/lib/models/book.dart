class Book {
  final String title;
  final String author;
  final int year;
  final String category;
  final String genre;
  final String shelf;
  final bool available;
  final String synopsis;
  final String cover;

  Book({
    required this.title,
    required this.author,
    required this.year,
    required this.category,
    required this.genre,
    required this.shelf,
    required this.available,
    required this.synopsis,
    required this.cover,
  });
}
